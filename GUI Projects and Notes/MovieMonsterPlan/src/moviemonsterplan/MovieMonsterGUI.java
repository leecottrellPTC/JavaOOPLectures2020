    private void loadAssets() {
        InputStream in;
        blankImage = tools.getImage(getClass().getResource("blank.png"));
        halloweenImage = tools.getImage(getClass().getResource("halloweenPoster.jpg"));
        alienImage = tools.getImage(getClass().getResource("alienPoster.jpg"));
        fridayImage = tools.getImage(getClass().getResource("friday13thPoster.jpg"));

        in = getClass().getResourceAsStream("halloween.wav");
        try {
            halloweenStream = new AudioStream(in);
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Halloween sound failed");
            System.exit(2);
        }

        in = getClass().getResourceAsStream("alien.wav");
        try {
            alienStream = new AudioStream(in);
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Alien sound failed");
            System.exit(2);
        }

        in = getClass().getResourceAsStream("friday13th.wav");
        try {
            fridayStream = new AudioStream(in);
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Friday sound failed");
            System.exit(2);
        }
    }

