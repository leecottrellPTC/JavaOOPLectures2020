private void playSound(AudioStream theStream){
        //demo without the stop
        AudioPlayer.player.stop(playingStream);
        playingStream = theStream;
        AudioPlayer.player.start(playingStream);
    }
     private void showImage(Image theImage){
       
        Image scaledMonster = theImage.getScaledInstance(imageLabel.getWidth(),
                imageLabel.getHeight(), Image.SCALE_SMOOTH);
        this.imageLabel.setIcon(new ImageIcon(scaledMonster));
    }
